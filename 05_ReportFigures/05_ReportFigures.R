#---------------------------------------------------------------------------------#
#--Identify OUs--#
OUs <- c('100-HR-3-D','100-HR-3-H','100-KR-4')
#---------------------------------------------------------------------------------#

#---------------------------------------------------------------------------------#
#--Loop Through Each OU--#
for (i in 1:length(OUs)){
  
  #---------------------------------------------------------------------------------#
  #--Extract Wells in OU--#
  AREA <- subset(WELL, select=c(NAME,OU))
  wells <- sort(unique(AREA[OU == OUs[i]]$NAME))
  #---------------------------------------------------------------------------------#
  
  #---------------------------------------------------------------------------------#
  #--Set File Name--#
  fn <- paste0(new_output_dir, 'TobitRegression_WLlag - ',OUs[i],'_CY2024_v3_032525_2.pdf')
  #---------------------------------------------------------------------------------#
  
  #---------------------------------------------------------------------------------#
  #--Start PDF Device--#
  pdf(fn,height=10,width=7.5,paper='letter')
  
  #---------------------------------------------------------------------------------#
  #--Loop Through Each Well--#
  for(j in 1:length(wells)){
   
    #---------------------------------------------------------------------------------#
    #--Extract Trend Analysis for Well--#
    X <- CHLAG[[wells[j]]]
    #---------------------------------------------------------------------------------#
    
    #---------------------------------------------------------------------------------#
    #--Print Well Name--#
    print(wells[j])
    #---------------------------------------------------------------------------------#
    
    #---------------------------------------------------------------------------------#
    #--Generate Plot if Trend Analysis Performed--#
    if(!is.null(X)){
      
      #---------------------------------------------------------------------------------#
      #--Extract by class--#
      if(class(X) == 'list'){
        
        #---------------------------------------------------------------------------------#
        #--Extract Analyte Name--#
        NM <- X[[1]]@NAME
        AN <- X[[1]]@DATA$ANALYTE[1]
        AN.UNITS <- X[[1]]@DATA[is.na(CHEM_UNITS)==F]$CHEM_UNITS[1]
        OU <- X[[1]]@DATA$OU[1]
        #---------------------------------------------------------------------------------#
        
        #---------------------------------------------------------------------------------#
        #--Set River stage usage--#
        if(NM %in% NEWRS$NAME){
          RS <- FALSE
        } else {
          RS <- TRUE
        }
        #---------------------------------------------------------------------------------#
        
        #---------------------------------------------------------------------------------#
        #--Extract Water-Level Data--#
        if(NM %in% names(WLLAG)){
          WL <- WLLAG[[NM]]
        } else {
          WL <- NULL
        }
        #---------------------------------------------------------------------------------#
        
        #---------------------------------------------------------------------------------#
        #--Extract Regression Information--#
        LAG <- NULL
        for (i in 1:length(X)){
          LSUB <- X[[i]]@LAG
          LAG <- c(LAG,LSUB)
        }
        #---------------------------------------------------------------------------------#
        
      } else if (class(X) == 'Trend_Summary'){
        
        #---------------------------------------------------------------------------------#
        #--Extract Analyte Name--#
        NM <- X@NAME
        AN <- X@DATA$ANALYTE[1]
        AN.UNITS <- X@DATA[!is.na(CHEM_UNITS)]$CHEM_UNITS[1]
        OU <- X@DATA$OU[1]
        #---------------------------------------------------------------------------------#
        
        #---------------------------------------------------------------------------------#
        #--Determine if River Stage Used--#
        if(NM %in% NEWRS$NAME){
          RS <- FALSE
        } else {
          RS <- TRUE
        }
        #---------------------------------------------------------------------------------#
        
        #---------------------------------------------------------------------------------#
        #--Extract Water-Level Data--#
        if(NM %in% names(WLLAG)){
          WL <- WLLAG[[NM]]
        } else {
          WL <- NULL
        }
        #---------------------------------------------------------------------------------#
        
        #---------------------------------------------------------------------------------#
        #--Extract Regression Information--#
        LAG <- X@LAG
        #---------------------------------------------------------------------------------#
        
      }
      #---------------------------------------------------------------------------------#
      
      #---------------------------------------------------------------------------------#
      #--Adjust Analyte Name if too Long--#
      if(AN == 'Hex. Chromium and Fil. Chromium'){AN = 'Hex. & Filt. Cr'}
      #---------------------------------------------------------------------------------#
      
      #---------------------------------------------------------------------------------#
      #--Generate Report Plot--#
      if(length(LAG) > 0){
        
        pltReport(X,WL,COORDS,BASE,OU=OU,ylab=bquote(.(AN) ~ ' ('*.(AN.UNITS)*')'),col=col,colline=colline,pch=pch,bg=bg,cex=0.7)
        
      } else {
        
        pltPlaceholder(X,WL,COORDS,BASE,OU=OU,ylab=bquote(.(AN) ~ ' ('*.(AN.UNITS)*')'),col=col,colline=colline,pch=pch,bg=bg,cex=0.7)
        
      }
      #---------------------------------------------------------------------------------#
      
    }
    #---------------------------------------------------------------------------------#
     
  }
  #---------------------------------------------------------------------------------#
  
  dev.off()
  #---------------------------------------------------------------------------------#
  
}
