#---------------------------------------------------------------------------------#
#--Set Colors--#
col <- c(rgb(46,139,87,255,maxColorValue=255),
         rgb(117,107,177,255,maxColorValue=255),
         rgb(77,77,77,255,maxColorValue=255),
         rgb(255,165,0,255,maxColorValue=255),
         rgb(0,0,205,255,maxColorValue=255))
colline <- c(rgb(46,139,87,255,maxColorValue=255),
             rgb(117,107,177,255,maxColorValue=255),
             rgb(77,77,77,255,maxColorValue=255),
             rgb(255,165,0,255,maxColorValue=255),
             rgb(0,0,205,255,maxColorValue=255))
pch <- c(21,21,21,21,21)
bg <- c(rgb(46,139,87,100,maxColorValue=255),
        rgb(117,107,177,100,maxColorValue=255),
        rgb(77,77,77,100,maxColorValue=255),
        rgb(255,165,0,100,maxColorValue=255),
        rgb(0,0,205,100,maxColorValue=255))
